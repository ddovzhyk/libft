#include "libft.h"

int		ft_atoi(const char *str)
{
	size_t	i;
	int		f;
	int		res;

	i = 0;
	res = 0;
	while ((str[i] == ' ') || (str[i] == '\r') || (str[i] == '\t') ||
		(str[i] == '\n') || (str[i] == '\v') || (str[i] == '\f'))
		i++;
	f = (str[i] == '-') ? -1 : 1;
	if ((str[i] == '-') || (str[i] == '+'))
		i++;
	while ((str[i] >= '0') && (str[i] <= '9'))
		res = (res * 10) + f * (str[i++] - '0');
	return (res);
}
